  private void agregarEstudiante() {
    try {
       
        String nombre = jTextField1.getText();
        String apellido = jTextField4.getText();
        double calificacionMatematicas = Double.parseDouble(jTextField2.getText());
        double calificacionHistoria = Double.parseDouble(jTextField3.getText());
        double calificacionCiencias = Double.parseDouble(jTextField5.getText());

        // Calcular el promedio de calificaciones
        double promedio = (calificacionMatematicas + calificacionHistoria + calificacionCiencias) / 3;

        
        String estatus = (promedio >= 6.0) ? "Aprobado" : "Reprobado";
        
        jTextField1.setText("");
        jTextField4.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField5.setText("");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error en los datos ingresados. Aseg칰rate de ingresar calificaciones v치lidas.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}








    
    
    
